package day5;

public class C5_UnaryDemo {
    public static void main(String[]args){
        int i = 5;
        i++;
        System.out.println(i);
        ++i;
        System.out.println(i);
        System.out.println(++i);// 8 pre increment operator
        System.out.println(i++); // post increment operator
        System.out.println(i);
    }
}
