package operators.arithmeticPrag;

public class UnaryIncrementDecrement {
    public static void main(String[]args){
        int i;
        i = 5;
        System.out.println(i);

        i++;
        System.out.println(i);
        ++i;
        System.out.println(i);
        i--;
        System.out.println(i);
        --i;
        System.out.println(i);



    }
}
